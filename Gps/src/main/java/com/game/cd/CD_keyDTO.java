package com.game.cd;

public class CD_keyDTO {
	private int cd_num;
	private int pro_num;
	private String cd_serial;
	private String cd_check;
	
	public int getCd_num() {
		return cd_num;
	}
	public void setCd_num(int cd_num) {
		this.cd_num = cd_num;
	}
	public int getPro_num() {
		return pro_num;
	}
	public void setPro_num(int pro_num) {
		this.pro_num = pro_num;
	}
	public String getCd_serial() {
		return cd_serial;
	}
	public void setCd_serial(String cd_serial) {
		this.cd_serial = cd_serial;
	}
	public String getCd_check() {
		return cd_check;
	}
	public void setCd_check(String cd_check) {
		this.cd_check = cd_check;
	}
	
}
